course = 'Python For Beginners'
          
print(course.upper()) #Make the sentence capitalized and print
print(course.lower()) #Make the sentence lowerized and print
print(course)
print(course.find('y')) #Gives index value of y
print(course.find('Y')) #Gives index value of Y . Here it does not exist so -1
print(course.find('For')) #Gives index value of word For
print(course.replace('For','4')) # Replace For with 4 
print('Python' in course) #In operator gives the search output as True or False
